<template lang="pug">
  section.section
    //コンポーネントでうまくできないので一旦こちらに
    v-layout(v-if="flash_message")
      v-alert.px-2.py-2.my-3(
        outline
        dismissible
        :value="true"
        type="success"
        ) {{ flash_message }}
    v-layout(justify-center)
      v-flex(xs12 md8)
        v-form
          v-text-field(
            label="タイトル"
            solo
            )
          v-textarea(
            placeholder="Instagramに載せたいハッシュタグまとめをご入力ください。"
            v-model="message"
            maxlength="500"
            auto-grow
            solo
            )
          v-container
            p.headline.text-xs-center.pink--text.font-weight-bold #の数：{{ now_hashtag_count }}

          v-layout(align-center justify-space-between row fill-height)
            v-btn(round) クリア
              v-icon clear
            v-btn(round)
              v-icon add_circle_outline
            v-btn.copy-button(
              round
              @click="onCopy(message)"
              )
              v-icon file_copy

    v-divider

    v-container
      h2.headline.text-xs-center.font-weight-thin
        | My hashtag set
    v-layout(justify-center)
      v-flex(xs12 md8)
        v-expansion-panel
            my-hashtag

</template>

<script>
import HashtagInput from '~/components/HashtagInput'
import MyHashtag from '~/components/MyHashtag'

export default {
  name: 'HomePage',

  data () {
    return {
      message:'',
      flash_message: '',
    };
  },

  components: {
    //HashtagInput
    MyHashtag
  },

  computed: {
    now_hashtag_count() {
      var input_text = this.message
      var hashtag_count = input_text.match(/#\S/g)
      if(hashtag_count){
        return hashtag_count.length;
      }else{
        return 0;
      }
    }
  },

  methods: {
    onCopy(msg) {
      this.$copyText(msg)
      return this.flash_message = "コピーしました"
    }
  }
}
</script>

